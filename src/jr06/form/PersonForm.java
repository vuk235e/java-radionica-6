    private void initPersonTable() {
        List<Person> persons = PersonRepository.getInstance().getPersons();
        PersonTableModel psm = new PersonTableModel(persons);
        tblPersons.setModel(psm);
        
        List<City> cities = new CityRepository().getCities();
        JComboBox<City> comboCity = new JComboBox<>();
        for (City city : cities) {
            comboCity.addItem(city);
        }
        
        TableColumnModel tcm = tblPersons.getColumnModel();
        TableColumn tcCity = tcm.getColumn(4);
        tcCity.setCellEditor(new DefaultCellEditor(comboCity));
        
        
    }
